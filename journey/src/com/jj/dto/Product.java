package com.jj.dto;

public class Product {
	int prod_no;
	String prod_name;
	String prod_sort;
	String prod_explain;
	int prod_price;
	String prod_city;
	public int getProd_no() {
		return prod_no;
	}
	public void setProd_no(int prod_no) {
		this.prod_no = prod_no;
	}
	public String getProd_name() {
		return prod_name;
	}
	public void setProd_name(String prod_name) {
		this.prod_name = prod_name;
	}
	public String getProd_sort() {
		return prod_sort;
	}
	public void setProd_sort(String prod_sort) {
		this.prod_sort = prod_sort;
	}
	public String getProd_explain() {
		return prod_explain;
	}
	public void setProd_explain(String prod_explain) {
		this.prod_explain = prod_explain;
	}
	public int getProd_price() {
		return prod_price;
	}
	public void setProd_price(int prod_price) {
		this.prod_price = prod_price;
	}
	public String getProd_city() {
		return prod_city;
	}
	public void setProd_city(String prod_city) {
		this.prod_city = prod_city;
	}
	
	
}
