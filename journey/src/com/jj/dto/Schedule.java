package com.jj.dto;

public class Schedule {
	int e_no;
	int sche_day;
	String place;
	public int getE_no() {
		return e_no;
	}
	public void setE_no(int e_no) {
		this.e_no = e_no;
	}
	public int getSche_day() {
		return sche_day;
	}
	public void setSche_day(int sche_day) {
		this.sche_day = sche_day;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	
}
