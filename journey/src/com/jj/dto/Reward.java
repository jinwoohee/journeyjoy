package com.jj.dto;

public class Reward {
	private int p_no;
	private int adult_fee;
	private int std_fee;
	private int child_fee;
	
	public int getP_no() {
		return p_no;
	}
	public void setP_no(int p_no) {
		this.p_no = p_no;
	}
	public int getAdult_fee() {
		return adult_fee;
	}
	public void setAdult_fee(int adult_fee) {
		this.adult_fee = adult_fee;
	}
	public int getStd_fee() {
		return std_fee;
	}
	public void setStd_fee(int std_fee) {
		this.std_fee = std_fee;
	}
	public int getChild_fee() {
		return child_fee;
	}
	public void setChild_fee(int child_fee) {
		this.child_fee = child_fee;
	}
}
