package com.jj.dto;

public class Ticket {
	int tick_no;
	int plac_no;
	String tick_name;
	String tick_sort;
	String tick_explan;
	int tick_price;
	String tick_file;
	
	public int getTick_no() {
		return tick_no;
	}
	public void setTick_no(int tick_no) {
		this.tick_no = tick_no;
	}
	public int getPlac_no() {
		return plac_no;
	}
	public void setPlac_no(int plac_no) {
		this.plac_no = plac_no;
	}
	public String getTick_name() {
		return tick_name;
	}
	public void setTick_name(String tick_name) {
		this.tick_name = tick_name;
	}
	public String getTick_sort() {
		return tick_sort;
	}
	public void setTick_sort(String tick_sort) {
		this.tick_sort = tick_sort;
	}
	public String getTick_explan() {
		return tick_explan;
	}
	public void setTick_explan(String tick_explan) {
		this.tick_explan = tick_explan;
	}
	public int getTick_price() {
		return tick_price;
	}
	public void setTick_price(int tick_price) {
		this.tick_price = tick_price;
	}
	public String getTick_file() {
		return tick_file;
	}
	public void setTick_file(String tick_file) {
		this.tick_file = tick_file;
	}
	
	
}
