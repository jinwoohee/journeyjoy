package com.jj.dto;

public class Account {
	int e_no;
	int acc_day;
	int acc_order;
	int acc_amount;
	String acc_payment;
	String acc_contents;
	String acc_category;
	String acc_currency;
	
	public int getE_no() {
		return e_no;
	}
	public void setE_no(int e_no) {
		this.e_no = e_no;
	}
	public int getAcc_day() {
		return acc_day;
	}
	public void setAcc_day(int acc_day) {
		this.acc_day = acc_day;
	}
	public int getAcc_order() {
		return acc_order;
	}
	public void setAcc_order(int acc_order) {
		this.acc_order = acc_order;
	}
	public int getAcc_amount() {
		return acc_amount;
	}
	public void setAcc_amount(int acc_amount) {
		this.acc_amount = acc_amount;
	}
	public String getAcc_payment() {
		return acc_payment;
	}
	public void setAcc_payment(String acc_payment) {
		this.acc_payment = acc_payment;
	}
	public String getAcc_contents() {
		return acc_contents;
	}
	public void setAcc_contents(String acc_contents) {
		this.acc_contents = acc_contents;
	}
	public String getAcc_category() {
		return acc_category;
	}
	public void setAcc_category(String acc_category) {
		this.acc_category = acc_category;
	}
	public String getAcc_currency() {
		return acc_currency;
	}
	public void setAcc_currency(String acc_currency) {
		this.acc_currency = acc_currency;
	}
	
	
}
